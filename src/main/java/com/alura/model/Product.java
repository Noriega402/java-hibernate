package com.alura.model;

import java.math.BigDecimal;
import java.time.LocalDate;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author Daniel Noriega
 */

@Entity
@Table(name="products")//asignar nombre a la DB, el nombre de la tabla debe de ser igual al nombre de la clase
public class Product {

    @Id
    @GeneratedValue(strategy=GenerationType.IDENTITY)//que id sea PRIMARY KEY
    private Long id;
    private String name;
    private String description;
    private BigDecimal price;
    private LocalDate registerDate = LocalDate.now();

    @ManyToOne // relacion de uno muchos a uno
    private Category category;

    public Product(){
        
    }
    
    public Product(String name, String description, BigDecimal price, Category category) {
        this.name = name;
        this.description = description;
        this.price = price;
        this.category = category;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }
    
    
}
